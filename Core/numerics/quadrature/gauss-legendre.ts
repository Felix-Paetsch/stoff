export function gauss_legendre(
    f: (x: number) => number,
    range: Interval = [0, 1],
    nodes: number = 5,
): number {
    nodes = Math.round(nodes);
    if (nodes < 1 || nodes > 16) {
        throw new RangeError(
            "gauss_legende: `nodes` must be an integer in [1, 16]",
        );
    }

    const [a, b] = range;

    if (!Number.isFinite(a) || !Number.isFinite(b)) {
        throw new RangeError(
            "gauss_legende: interval bounds must be finite numbers",
        );
    }

    if (a === b) {
        return 0;
    }

    const table = GAUSS_LEGENDRE_TABLES[nodes]!;
    const mid = (a + b) / 2;
    const half = (b - a) / 2;

    let sum = 0;

    for (let i = 0; i < table.nodes.length; i++) {
        const t = table.nodes[i]!;
        const w = table.weights[i]!;

        if (t === 0) {
            sum += w * f(mid);
        } else {
            const dx = half * t;
            sum += w * (f(mid - dx) + f(mid + dx));
        }
    }

    return half * sum;
}
