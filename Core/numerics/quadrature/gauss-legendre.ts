/*
 * Gauss-Legendre quadrature on a finite interval.
 *
 * `nodes` must be in [1, 16].
 *
 * The precomputed tables below are for the standard interval [-1, 1].
 * We map them to [a, b] via:
 *
 *   x = mid + half * t
 *
 * where
 *   mid = (a + b) / 2
 *   half = (b - a) / 2
 *
 * Then:
 *
 *   ∫[a,b] f(x) dx = half * Σ w_i * f(mid + half * t_i)
 *
 * Because the nodes/weights are symmetric, we store only the nonnegative
 * nodes and their corresponding weights.
 */

